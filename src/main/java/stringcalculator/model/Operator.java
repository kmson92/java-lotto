package stringcalculator.model;

public class Operator {
    public static int add(int i, int j) {
        return i + j;
    }

    public static int minus(int i, int j) {
        return i - j;
    }

    public static int multiply(int i, int j) {
        return i * j;
    }

    public static int division(int i, int j) {
        return i / j;
    }
}
